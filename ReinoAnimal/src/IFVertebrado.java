
public abstract class IFVertebrado {
    private boolean esqueleto;

    public IFVertebrado(boolean esqueleto) {
        this.esqueleto = esqueleto;
    }

    public boolean getEsqueleto() {
        return esqueleto;
    }
    public void setEsqueleto(boolean esqueleto) {
        this.esqueleto = esqueleto;
    }


}
