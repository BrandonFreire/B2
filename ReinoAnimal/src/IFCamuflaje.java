public class IFCamuflaje {
    private String color;

    /**
     * Constructor sin parametros 
     */
    public IFCamuflaje() {
    }
    /**
     * Constructor con parametros
     * @param color
     */
    public IFCamuflaje(String color) {
        this.color = color;
    }
    /**
     * getter/setter
     * @return
     */
    public String getColor() {
        return color;
    }
    public void setColor(String color) {
        this.color = color;
    }



}
